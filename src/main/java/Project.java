public class Project {

    private int ID;
    private String name;
    private int BUID;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getBUID() {
        return BUID;
    }

    public void setBUID(int BUID) {
        this.BUID = BUID;
    }
}
